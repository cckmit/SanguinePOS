    private void funOpenCustomerMasterHelp()
    {
        objUtility.funCallForSearchForm("CustomerMaster");
        new frmSearchFormDialog(null, true).setVisible(true);
        if (clsGlobalVarClass.gSearchItemClicked)
        {
            Object[] data = clsGlobalVarClass.gArrListSearchData.toArray();
            //System.out.println("Cust Mb No= "+data[2]);
            clsGlobalVarClass.gSearchItemClicked = false;

            String[] mobileNos = data[2].toString().split(",");
            if (mobileNos.length > 0)
            {
                txtDisplayNumber.setText(mobileNos[0]);
            }
            else
            {
                txtDisplayNumber.setText("");
            }
            funOKButtonClicked();
        }
    }

