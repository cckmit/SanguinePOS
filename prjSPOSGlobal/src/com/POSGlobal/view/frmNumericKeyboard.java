    public void funPressEnterBtn()
    {
        try
        {
            /*
             if ((!flgFormType.equals("homedelivery")) || (!flgFormType.equals("master"))) 
             {
             if(txtDisplayNumber.getText().isEmpty())
             {
             clsGlobalVarClass.gNumerickeyboardValue = "0";
             }
             else
             {
             if (!clsGlobalVarClass.validateNumbers(txtDisplayNumber.getText().trim())) {
             new frmOkPopUp(null, "Please Enter Numbers Only", "Error", 1).setVisible(true);
             return;
             }
             clsGlobalVarClass.gNumerickeyboardValue=String.valueOf(Math.abs(Double.parseDouble(txtDisplayNumber.getText())));
             //clsGlobalVarClass.gNumerickeyboardValue=String.valueOf(Double.parseDouble(txtDisplayNumber.getText()));
             }
             }*/

            if (txtDisplayNumber.getText().isEmpty())
            {
                clsGlobalVarClass.gNumerickeyboardValue = "0";
            }
            else
            {
                if (!clsGlobalVarClass.validateNumbers(txtDisplayNumber.getText().trim()))
                {
                    new frmOkPopUp(null, "Please Enter Numbers Only", "Error", 1).setVisible(true);
                    return;
                }
                if (type.equalsIgnoreCase("Double"))
                {
                    clsGlobalVarClass.gNumerickeyboardValue = String.valueOf(Math.abs(Double.parseDouble(txtDisplayNumber.getText())));
                }
                else
                {
                    clsGlobalVarClass.gNumerickeyboardValue = String.valueOf(Long.parseLong(txtDisplayNumber.getText()));
                }
            }

            dispose();
            objUtility = null;

        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }

