    private void funSaveRecords()
    {
        try
        {
            String sql;
            sql="delete from tbltransbtnsequence where strTransactionName='"+cmbType.getSelectedItem()+"'";
            clsGlobalVarClass.dbMysql.execute(sql);
            String transName=cmbType.getSelectedItem().toString();
            Map<String,String> hmTransSeq=new HashMap<String,String>();
            
            int cnt1=0;
            for(int cnt=0;cnt<tblArrangeTrans.getRowCount();cnt++)
            {                
                hmTransSeq.put(tblArrangeTrans.getValueAt(cnt1,1).toString(),tblArrangeTrans.getValueAt(cnt1,0).toString());
                cnt1++;
            }
            
            String sqlInsert1="insert into tbltransbtnsequence( ";
            String sqlInsert2="values( ";
            
            for(Map.Entry entry:hmTransSeq.entrySet())
            {
                sqlInsert1+=entry.getKey()+",";
                sqlInsert2+=entry.getValue()+",";
            }
            sqlInsert1+="strTransactionName,strClientCode,strDataPostFlag)";
            sqlInsert2+="'"+transName+"','"+clsGlobalVarClass.gClientCode+"','N')";
            
            //System.out.println(sqlInsert1+sqlInsert2);
            clsGlobalVarClass.dbMysql.execute(sqlInsert1+sqlInsert2);
            JOptionPane.showMessageDialog(null, "Data Insert Successfully");
            
        } catch (Exception ex) {
            objUtility.funWriteErrorLog(ex);
             ex.printStackTrace();
        }
    }
    
    
    
