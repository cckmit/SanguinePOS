     private void funFillTransButtonGrid(String type){
        try
        {
            String sql;
            dmArrageTrans = (DefaultTableModel) tblArrangeTrans.getModel();
            dmArrageTrans.setRowCount(0);
            sql = "select * from tbltransbtnsequence "
                + "where strTransactionName='" + type + "' ";
            ResultSet rsFillGrid = clsGlobalVarClass.dbMysql.executeResultSet(sql);
            TreeMap<Integer, String> hm = new TreeMap<Integer, String>();
            if (rsFillGrid.next())
            {
                hm.put(Integer.parseInt(rsFillGrid.getString(1)), "strPLUButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(2)), "strSettleBillButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(3)), "strDeliveryBoyButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(4)), "strHomeDeliveryButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(5)), "strTakeAwayButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(6)), "strCheckKOTButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(7)), "strModifierButton");
                hm.put(Integer.parseInt(rsFillGrid.getString(8)), "strNCKOTButton");
            }
            
            for (Map.Entry m : hm.entrySet())
            {
                Object[] ob =
                {
                    m.getKey(), m.getValue()
                };
                dmArrageTrans.addRow(ob);
            }
            tblArrangeTrans.setModel(dmArrageTrans);

        }
        catch (Exception ex) {
            objUtility.funWriteErrorLog(ex);
            ex.printStackTrace();
        }
   }
