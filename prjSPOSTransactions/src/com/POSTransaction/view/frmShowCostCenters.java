    public void funFillGridWithItems() throws Exception
    {

	DefaultTableModel dtm = (DefaultTableModel) tblRewards.getModel();
	dtm.setRowCount(0);

	String sql = "select a.strCostCenterCode,a.strCostCenterName  "
		+ "from tblcostcentermaster a ";
	ResultSet rsCostCenter = clsGlobalVarClass.dbMysql.executeResultSet(sql);
	while (rsCostCenter.next())
	{

	    Object[] ob =
	    {
		rsCostCenter.getString(1), rsCostCenter.getString(2), false
	    };
	    dtm.addRow(ob);
	}
	rsCostCenter.close();

	DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
	rightRenderer.setHorizontalAlignment(JLabel.RIGHT);

	tblRewards.setModel(dtm);
	tblRewards.setRowHeight(40);

	tblRewards.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

    }

