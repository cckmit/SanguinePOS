    private void funOKButtonClicked()
    {
	try
	{
	    List<clsCostCenterBean> listOfSelectedCostCenters = new ArrayList<>();

	    boolean isCostCenterSelected = false;
	    for (int r = 0; r < tblRewards.getRowCount(); r++)
	    {
		boolean isSelect = Boolean.parseBoolean(tblRewards.getValueAt(r, 2).toString());
		if (isSelect)
		{
		    isCostCenterSelected = true;
		    clsCostCenterBean objCostCenter = new clsCostCenterBean();
		    objCostCenter.setStrCostCenterCode(tblRewards.getValueAt(r, 0).toString());
		    objCostCenter.setStrCostCenterName(tblRewards.getValueAt(r, 1).toString());

		    listOfSelectedCostCenters.add(objCostCenter);
		}
		else
		{
		    continue;
		}
	    }

	    if (isCostCenterSelected)
	    {
		objKDSForKOT1366x768Resolution.funSetSelectedCostCenter(listOfSelectedCostCenters);
		
		dispose();
		objKDSForKOT1366x768Resolution.setVisible(true);
	    }
	    else
	    {
		JOptionPane.showMessageDialog(this, "Please select the cost center.");
		return;
	    }	 
	}
	catch (Exception e)
	{
	    e.printStackTrace();
	}
    }

