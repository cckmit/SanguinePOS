    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // TODO add your handling code here:
        try
        {
            int Exec = 0;
            for (int i = 0; i < tblDesktop.getRowCount(); i++)
            {
                if (clsGlobalVarClass.gSuperUser == true)
                {
                    sql = "update tblsuperuserdtl set intSequence='" + tblDesktop.getValueAt(i, 0) + "' where strFormName='" + tblDesktop.getValueAt(i, 1) + "' and strUserCode='" + clsGlobalVarClass.gUserCode + "'";
                }
                else
                {
                    sql = "update tbluserdtl set intSequence='" + tblDesktop.getValueAt(i, 0) + "' where strFormName='" + tblDesktop.getValueAt(i, 1) + "' and strUserCode='" + clsGlobalVarClass.gUserCode + "'";
                }
                Exec = clsGlobalVarClass.dbMysql.execute(sql);
            }
            if (Exec > 0)
            {
                new frmOkPopUp(this, "Sequence Save Successfully", "Successful", 3).setVisible(true);
                funLoadTable();
            }

        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Invalid Selection");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveMouseClicked

