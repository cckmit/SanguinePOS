    private void funLoadTable()
    {
        try
        {
            dm1 = (DefaultTableModel) tblDesktop.getModel();
            dm1.setRowCount(0);
            if (clsGlobalVarClass.gSuperUser == true)
            {
                sql = "select a.intSequence,a.strFormName from tblsuperuserdtl a left outer join tblforms b on a.strFormName=b.strModuleName ";

                if (clsGlobalVarClass.gSelectedModule.equalsIgnoreCase("Masters"))
                {
                    sql = sql + " where a.strUserCode='" + clsGlobalVarClass.gUserCode + "' and b.strModuleType='M'";
                }
                else if (clsGlobalVarClass.gSelectedModule.equalsIgnoreCase("Transactions"))
                {
                    sql = sql + " where a.strUserCode='" + clsGlobalVarClass.gUserCode + "' and b.strModuleType='T'";
                }
                else if (clsGlobalVarClass.gSelectedModule.equalsIgnoreCase("Reports"))
                {
                    sql = sql + " where a.strUserCode='" + clsGlobalVarClass.gUserCode + "' and b.strModuleType='R'";
                }
                sql = sql + " order by a.intSequence;";
            }
            else
            {
                sql = "select a.intSequence,a.strFormName from tbluserdtl a left outer join tblforms b on a.strFormName=b.strModuleName ";

                if (clsGlobalVarClass.gSelectedModule.equalsIgnoreCase("Masters"))
                {
                    sql = sql + " where a.strUserCode='" + clsGlobalVarClass.gUserCode + "' and b.strModuleType='M'";
                }
                else if (clsGlobalVarClass.gSelectedModule.equalsIgnoreCase("Transactions"))
                {
                    sql = sql + " where a.strUserCode='" + clsGlobalVarClass.gUserCode + "' and b.strModuleType='T'";
                }
                else if (clsGlobalVarClass.gSelectedModule.equalsIgnoreCase("Reports"))
                {
                    sql = sql + " where a.strUserCode='" + clsGlobalVarClass.gUserCode + "' and b.strModuleType='R'";
                }
                sql = sql + " order by a.intSequence;";
            }
            System.out.println(sql);
            rs = clsGlobalVarClass.dbMysql.executeResultSet(sql);
            int seq = 1;
            while (rs.next())
            {
                // Object[] ob = {rs.getString(1), rs.getString(2)};
                Object[] ob =
                {
                    seq, rs.getString(2)
                };
                dm1.addRow(ob);
                seq++;
            }
            rs.close();
            tblDesktop.setModel(dm1);
        }
        catch (Exception e)
        {
        }
    }
