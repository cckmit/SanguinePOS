    private void lblMoveDownMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblMoveDownMouseClicked
        if (tblDesktop.getRowCount() == SelectedFormRowNo + 1)
        {
            // do Nothing This is last row
        }
        else
        {
            String tempSelectedFormName = tblDesktop.getValueAt(SelectedFormRowNo, 1).toString();
            String tempLowerFormName = tblDesktop.getValueAt(SelectedFormRowNo + 1, 1).toString();
            tblDesktop.setValueAt(tempSelectedFormName, SelectedFormRowNo + 1, 1);
            tblDesktop.setValueAt(tempLowerFormName, SelectedFormRowNo, 1);
            SelectedFormRowNo = SelectedFormRowNo + 1;
            tblDesktop.setRowSelectionInterval(SelectedFormRowNo, SelectedFormRowNo);

        }
    }//GEN-LAST:event_lblMoveDownMouseClicked

